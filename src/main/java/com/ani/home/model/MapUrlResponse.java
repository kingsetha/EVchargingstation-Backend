package com.ani.home.model;

public class MapUrlResponse {

    private String mapUrl;

    // Constructors
    public MapUrlResponse() {}

    public MapUrlResponse(String mapUrl) {
        this.mapUrl = mapUrl;
    }

    // Getter and Setter
    public String getMapUrl() {
        return mapUrl;
    }

    public void setMapUrl(String mapUrl) {
        this.mapUrl = mapUrl;
    }
}
